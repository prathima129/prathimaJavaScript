// In JS operators are used to perform operations on variables and values

//1.Arithmetic Operator

let a =5;
let b =6;
console.log(a+b) //Addition
console.log(a-b) //Subtraction
console.log(a*b) //Multiplication
console.log(a/b) //Division
console.log(a%b) //Modulus(Reminder)
console.log(a**b) //a power b
console.log(a++) //Increment by +1
console.log(a--) //Decrement by -1 

//2.Assignment Operator

let c=10;
let d=5;

//Add and Assign
c+=d; //c=c+d->c=15
console.log(c);
 
//Subtract and Assign
c-=d; //c=c-d->c=10
console.log(c);

//Multiply and Assign
c*=d; //c=c*d->c=50
console.log(c);

//Divide and Assign
c/=d; //c=c/d->c=10
console.log(c);

//Modulus and Assign
c%=d; //c=c%d->c=0
console.log(c);

//Exponentiation and Assign
c=2;
c**d; //c=c**d->2^5 = 32
console.log(c);
